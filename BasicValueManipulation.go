package main
import "fmt"
func main(){
// strings added together with "+" are concatenated whereas those added with , are simply printed together with a space in between.
fmt.Println("GO"+"lang", "tutorial")
//Integer and Float types
fmt.Println("2+2 = ",2+2)
fmt.Println("2*2 = ",2*2)
fmt.Println("12/5 = ", 12/5)
fmt.Println("12.0/5.0 =", 12.0/5.0)
}
